// ========== FONCTIONS GLOBALES ==========
function t(key, params = {}) {
    let text = eMarketI18n.translations[eMarketI18n.currentLang]?.[key] || eMarketI18n.translations.fr[key] || key;
    for (const [k, v] of Object.entries(params)) text = text.replace(`{${k}}`, v);
    return text;
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) {
            if (el.tagName === 'INPUT' && el.getAttribute('data-i18n-placeholder')) el.placeholder = t(key);
            else el.innerHTML = t(key);
        }
    });
    document.querySelectorAll('select option').forEach(opt => {
        const key = opt.getAttribute('data-i18n');
        if (key) opt.textContent = t(key);
    });
    // Mise à jour des produits si la fonction existe
    if (typeof emarketRenderProducts === 'function') emarketRenderProducts();
    if (typeof emarketUpdateCustomerUI === 'function' && window.currentCustomer) emarketUpdateCustomerUI();
}

function changeLanguage(lang) {
    if (eMarketI18n.translations[lang]) {
        eMarketI18n.currentLang = lang;
        localStorage.setItem('emarket_lang', lang);
        applyTranslations();
    }
}