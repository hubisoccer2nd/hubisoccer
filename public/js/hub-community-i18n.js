let hubiCurrentLang = localStorage.getItem('hubiLang') || navigator.language.split('-')[0];
if (!hubCommunityTranslations[hubiCurrentLang]) hubiCurrentLang = 'fr';

function hubiT(key, params = {}) {
    let text = hubCommunityTranslations[hubiCurrentLang]?.[key] || hubCommunityTranslations.fr[key] || key;
    for (const [k, v] of Object.entries(params)) text = text.replace(`{${k}}`, v);
    return text;
}

function hubiSetLanguage(lang) {
    if (hubCommunityTranslations[lang]) {
        hubiCurrentLang = lang;
        localStorage.setItem('hubiLang', lang);
        if (typeof applyHubiTranslations === 'function') applyHubiTranslations();
    }
}

function applyHubiTranslations(scope = document) {
    scope.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) {
            if (el.tagName === 'INPUT' && el.hasAttribute('data-i18n-placeholder')) {
                el.placeholder = hubiT(key);
            } else {
                el.innerHTML = hubiT(key);
            }
        }
    });
}
