let tournoiCurrentLang = localStorage.getItem('tournoi_lang') || navigator.language.split('-')[0];
if (!tournoiTranslations[tournoiCurrentLang]) tournoiCurrentLang = 'fr';

function tournoiT(key, params = {}) {
    let text = tournoiTranslations[tournoiCurrentLang]?.[key] || tournoiTranslations.fr[key] || key;
    for (const [k, v] of Object.entries(params)) text = text.replace(`{${k}}`, v);
    return text;
}

function tournoiSetLanguage(lang) {
    if (tournoiTranslations[lang]) {
        tournoiCurrentLang = lang;
        localStorage.setItem('tournoi_lang', lang);
        if (typeof applyTournoiTranslations === 'function') applyTournoiTranslations();
    }
}

function applyTournoiTranslations(scope = document) {
    scope.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) {
            if (el.tagName === 'INPUT' && el.hasAttribute('data-i18n-placeholder')) {
                el.placeholder = tournoiT(key);
            } else {
                el.innerHTML = tournoiT(key);
            }
        }
    });
}
